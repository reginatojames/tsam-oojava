
public class Persona {
	int altezza;
	int peso;
	int eta;
	String attivitaFisica;
	char sesso;
	
	Persona (int altezza, int peso, int eta, String attivitaFisica, char sesso){
		this.altezza = altezza;
		this.peso = peso;
		this.eta = eta;
		this.attivitaFisica = attivitaFisica;
		this.sesso = sesso;
	}
}
